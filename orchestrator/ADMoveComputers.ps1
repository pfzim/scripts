$comps = Get-ADComputer -SearchBase "OU=Новые компьютеры,OU=ТОФы,OU=Company,DC=contoso,DC=com" -Filter *

foreach($comp in $comps)
{
    if($comp.Name -match "\d\d\d\d.*")
    {
        $region = $comp.Name.Substring(0, 2)
        if($list[$region])
        {
            Move-ADObject -Identity $comp -TargetPath $list[$region]
        }
    }
}


